const NUM_FRACTION_DIGITS = 1;

const formatNumber = (num: number) => {
  const lookup = [
    { value: 1, symbol: "" },
    { value: 1e3, symbol: "k" },
    { value: 1e6, symbol: "M" },
    { value: 1e9, symbol: "B" },
  ];
  const rx = /\.0+$|(\.[0-9]*[1-9])0+$/;
  const item = lookup
    .slice()
    .reverse()
    .find(function (item) {
      return num >= item.value;
    });
  return item
    ? (num / item.value).toFixed(NUM_FRACTION_DIGITS).replace(rx, "$1") +
        item.symbol
    : "0";
};

export default formatNumber;
